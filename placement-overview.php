						<p>An integral part of the career development programme is placement, which is undertaken by Training and Placement Cell (TPC) of the University which operates the Corporate Resource Centre (CRC) to provide the students with training, counselling, arrange guest lectures and visits, placement drives and interviews. The function of the CRC is actively intertwined with the academics to build up the overall capability of the students as a confident and amicable person, ready to take on any challenge the world may throw upon, not merely for a job.</p>	
                        <p>The manifoldnature of the placement process includes personality development, gap identification, mock interview, pre- placement counseling, CV formation, and live projects and internships. During their academic tenure the students go through a well-planned rigorous course curriculum along with real life practical trainings and internships in reputed research institutes and/or corporate houses.</p>
                        <p>The academic curriculum is so structured that it requires the students to undergo a mandatory summer internship of 6-8 weeks with reputed institutes/industrial organizations. The internship helps the students to apply their classroom learning of theoretical concepts to real life management settings. This process enables to sharpen their ability to diagnose, analyze and resolve problems. The option of short term live projects with leading companies is also provided to the students. This runs parallel to their academic sessions and gives them valuable insight into the workings of the industry.</p>
                        <p>At CSMU, the focus is on the overall development of an individual. This is achieved by the CRC, which lays a lot of emphasis on grooming and helps each student refine his/her communication and behavioral skills, thereby making them good fits for the industry. The academic environment takes advantage of the world class, state of the art infrastructure provided at CSMU. Not to be left behind are the immense opportunities for extracurricular activities, leading to the holistic growth of each individual.
Since its inception, CSMU continues to maintain a consistently impressive placement record. It has been able to assist students to pursue careers in their dream companies and become successful leaders. It continues to do so each year, moving from strength to strength surely and consistently.</p>
					
