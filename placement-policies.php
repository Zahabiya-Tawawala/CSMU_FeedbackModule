						<p><strong>Chhatrapati Shivaji Maharaj University Placement policy is defined by three major roles:</strong></p>
					
						<div class="heading heading-border heading-middle-border mt-0 mb-4" id="">
        <h4><strong> Role of Placement Cell</strong></h4>
      </div>
<ul>
  <li>The CRC (which is  the nodal centre for placements at Chhatrapati Shivaji Maharaj University  sends invitation to companies/organizations along with the relevant  information.</li>
  <li>Classification of  Companies will be based on employment sector, salary packages, brand-value of  company, relationship &amp; past record of recruitment at Chhatrapati Shivaji Maharaj University.</li>
  <li>CRC will obtain all  information available on Internet or physical visit to the company if required,  to check the profile and track record of the company prior conducting the  campus drive.</li>
  <li>CRC will ask the  company to share the Job Description/Job Announcement, Details of Organization,  Job Profile, Salary Details, Selection Process, Minimum Qualification Required,  Probation Period, Documents required for Joining, Tentative Date of Joining  etc.</li>
  <li>The details  provided by the companies through Job Description and company brochures will be  displayed in CRC and these details will also be shared with the concerned  school Placement co-coordinators and their respective students.</li>
  <li>With mutual consent  of respective Head of Departments, the Placement Department/ Cell will suggest  two or three dates to companies/organizations to conduct campus or off-campus  drive (depending on total participating student numbers for particular drive).</li>
  <li>The company/organization  will choose one date from the suggested dates and will inform to the Placement  department.</li>
  <li>CRC official will  convey the confirmed date via official email to head of the Departments,  placement coordinator of respective Departments and to the students applying  for the respective company.</li>
  <li>Company will visit  the campus or invite them to their respective office (in case of off-campus  drive) on the allotted date to conduct tests and/or interviews according to  their recruitment process.</li>
  <li>CRC officials will  conduct mock GD/PI//PD sessions two days before the recruitment drive. This  will help the students to build up their confidence.</li>
  <li>The  company/organization shall be requested to furnish the final list of selected  students on the same day of campus drive or after the document verification</li>
  <li>The company shall  hand over the duly signed hard copy or soft copy via official email id of the  final list of selected candidates to the CRC.</li>
  <li>The company shall  provide the offer letters to CRC department and not directly to the students.</li>
  <li>For any campus  drive, CRC will make necessary arrangements for hospitality of executives.</li>
  <li>After placement  drives, results should be displayed on placement cell notice boards.</li>
</ul>
<div class="heading heading-border heading-middle-border mt-5 mb-2" id="">
        <h4><strong>Role of Departments in CSMU</strong></h4>
      </div>
<ul>
  <li>Placement form/  Student Declaration form provided by CRC, will be filled by final year students  of all Departments.</li>
  <li>Data bank of each  student is to be kept ready by the Department placement coordinator/ HOD and to  be forwarded to the placement department.</li>
  <li>Once the respective  Department received the job description from CRC, then within three days  Department placement coordinator shall provide the details of participants to  the CRC office.</li>
  <li>The job description  provided by the CRC will be displayed at notice board of concerned Department.</li>
  <li>Regular and  frequent communication between the Head of Department and Executives of CRC is advised.</li>
  <li>If any CSMU  Department is organizing the campus drive, an advance notification is to be  sent to CRC also the complete job description is to be shared with CRC office.</li>
  <li>The concerned department will issue the NOC (No Objection Certificate)  to the selected students prior joining the company in consideration with TPO.</li>
</ul>
<div class="heading heading-border heading-middle-border mt-5 mb-2" id="">
<h4><strong>Student Guidelines for Placements</strong></h4>
</div>
<ul>
	<li>All students of final year are eligible to participate in the placement drives however, they shall be permitted to join the assignment only in last semester.</li>
    <li>Students have to ensure that they report on time as informed by CRC team/coordinators. Students coming late will be strictly not allowed to appear in that process.</li>
    <li>All students need to ensure that they are meeting the eligibility criteria of the company as mentioned in the Job Description. All mark sheets are checked at the time of joining and if company founds that you have manipulated your marks / documents at the time of Campus Recruitment your job offer will be terminated and you name will be blacklisted forever.</li>
    <li>It is mandatory to maintain 75% attendance in concurrent semester to appear for any campus placement.</li>
    <li>Strict discipline need to be followed by every student during the recruitment process. Any students found misbehaving or during misconduct will be debarred from the entire placement season. No casual talks or remarks should be extended by any students during the process or even after the process to company delegates</li>
    <li>Students are not allowed to contact Company officials directly for any reason. If they are facing any problem they have to first contact their Placement coordinator/officer available and discuss their problem with them. Respective Placement Officers may consult the Placement Head to resolve the issues instead of bringing insignificant issues before company representatives.</li>
    <li>One student One Offer policy is followed at CSMU. Exceptions to this rule, as below:
    	<ul>
  <li>Placed students can  appear for product based company with Higher Package as Dream Status Company.</li>
  <li>Once a student is placed in a core company, he/she will not be  allowed to appear for placement of any other company. However, the TPO may  allow the student to appear provided satisfactory reason.</li>
  <li>Students may get  multiple offers due to non-disclosure of the result in time by the company.</li>
</ul>
    </li>
    <li>Proper diligence should be done before accepting or declining an once the offer letter is accepted by the student, thereafter if he/she declines the acceptance then such student will be debarred and will not be allowed to appear in any other future placement events, except in genuine and special circumstances.</li>
    <li>If student's name appears on the final shortlist declared after the Company's selection process through the Placement Cell, then that would be considered as an offer to the student.</li>
    <li>If any student quits in between, he/she will be debarred & will not be allowed to appear in any other future placement events, except in special circumstances</li>
    
</ul>
								
