      <p>Chhatrapati Shivaji Maharaj University has become a prominent institute for law education in a short time, a feat accredited to its vibrant Department of Law (DoL).The Department aims to enrich legal education by providing an environment in which law students can begin to learn the art of lawyering by serving real clients, and can develop the habit of reflecting on how to improve themselves, the profession, and the legal system. DoL is approved by the Bar Council of India (BCI) to run the Graduate LL. B. programme. For those who, at an early age aim to enter the world of legal education can opt for the BCI-approved 5-year integrated B.A. (Hons.) - L.L.B. programme.</p>
      <p>A successful business administrator must have keen knowledge of legal proceedings. The department of law, with the approval of the BCI, offers the B.B.A-LL.B. Integrated programme to churn out business masterminds with a legal perception.</p>
      <p>The field of law and legal education is a constantly-evolving and highly dynamic realm where learning never stops. DoL offers postgraduate and doctoral courses in law for those who strive to achieve masterful expertise in a branch or sub-discipline of law.</p>
      <p>The LLM program at Department of Law, CSMU currently offers four specializations, considering the contemporary state of legal affairs, viz: Business Law, Criminology, Constitutional Law and Human Rights.</p>

      <p>The faculty members at Department of Law are highly qualified and experienced and have advance degrees in Law from renowned Central and State	Universities. To foster an educational environment we allow the student to strive for the highest level of practice, providing each student with a caseload that involves a varied group of problems, legal issues, and lawyering skills. Our professional emphasis is on "client-centered representation," in which the student lawyer focuses on the client's goals, rather than defining what the client needs; and treats the client as an effective collaborator in problem-solving. Our educational goal is to graduate lawyers who know what excellent legal practice should be; and who have developed a habit of thinking about the complexities of the law and the larger implications of the work they do, making	them life-long learners.</p>
      <p>The Department of Law has continued to change to meet needs of students, clients, community, and the Law School. In particular, we are proactive about constantly assessing and revising our educational methods. The Department of Law strives constantly to foster the best possible educational environment; still, in the end, it is the students who make their own education. Other regular features of Department of Law are Moot Court, Legal Aid Clinic, Invited Talks, Conferences, Seminars, Workshops and Quiz competitions etc.</p>
