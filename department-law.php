      <p>Law can be defined as the rules established in a community under the authority of the state or nation and applicable to its people. This could be in the form of legislation or of customs and policies which are acknowledged and enforced by judicial decision. A lawyer is someone who studies Law and gains knowledge regarding all the rules and regulations. It is the lawyer's job to advise the people about the law and to also speak for them in court.</p>
