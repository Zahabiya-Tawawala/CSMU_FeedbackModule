      <p>Department of Law @ CHHATRAPATI SHIVAJI MAHARAJ UNIVERSITY has a dynamic, vibrant and active mooting culture. The Moot Court Committee (MCC) is a student led initiative, which works hand in hand with the administration for planning, training and executing its activities. The MCC hopes to create a strong tradition of successful mooting for years to come for	the	students.</p>
      <p>The students represent the University in national and international moot court competitions where students get a chance to learn new subjects, polish their advocacy skills, build networks, and	make	friends. The MCC, under the aegis of Faculties and the MCC Board, have organized various remarkable National Moot Court Competitions which have been admired and graced by various distinguished legal luminaries.</p>

      
