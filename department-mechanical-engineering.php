      <p>Mechanical engineering is a broad engineering discipline that incorporates skills and expertise in the areas of design, manufacturing, mechanics and thermal sciences that are essential to most sectors of industry. It is one of the oldest and broadest of the engineering disciplines and one of the most preferred one by the engineering aspirants.</p>
	  <p>The department offers undergraduate, Post graduate and Ph.D programmes. Undergraduate course in Mechanical Engineering &amp; Postgraduate courses are offered in Machine Design and Product Design &amp; Manufacturing. In addition to this M.Tech and Ph.D courses are offered for those who are working in institution and industry through Research and Development center.</p>
	  <p>Laboratories in the department are well equipped to facilitate the conduction of experiments prescribed in the curriculum and also to carry out the research work. Some of the research facilities like, Dynamic Mechanical Analyzer (DMA), Optical microscope, 3D printer, and Hydraulic press etc. are utilized by MSc and Ph.D research scholars of our institution and other institutions.</p>
	  <p>The department is continuously organizing the industrial visits, seminars, technical talk and placement training for the benefits of the students.</p>
   <br> <p>Mechatronics engineering, is a multidisciplinary branch of engineering that focuses on the engineering of both electrical and mechanical systems, and also includes a combination of robotics, electronics, computer, telecommunications, systems, control, and product engineering. As technology advances over time, various subfields of engineering have succeeded in both adapting and multiplying. The intention of Mechatronics is to produce a design solution that unifies each of these various subfields. Originally, the field of Mechatronics was intended to be nothing more than a combination of mechanics and electronics, hence the name being a portmanteau of mechanics and electronics; however, as the complexity of technical systems continued to evolve, the definition had been broadened to include more technical areas.A Mechatronics engineer unites the principles of mechanics, electronics, and computing to generate a simpler, more economical and reliable system. The term "Mechatronics" was coined by Tetsuro Mori, the senior engineer of the Japanese company Yaskawa in 1969. An industrial robot is a prime example of a Mechatronics system; it includes aspects of electronics, mechanics, and computing to do its day-to-day jobs.</p>

