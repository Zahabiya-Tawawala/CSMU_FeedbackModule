                                    </div>
                                    <p>The objective of the Consultancy Policy is to: establish a framework to support consultancy activities and services at the Chhatrapati Shivaji Maharaj University.</p>
                                    <p>This policy applies to all staff, of the University who are involved in the conduct of a consultancy service and all funds provided to the university for the purpose of conducting or supporting a consultancy service.</p>
                                    <p>The Consultancy Policy of the Chhatrapati Shivaji Maharaj University recognises that consultancy work can bring financial and other benefits both to the institution and its employees. Such activities also serve to link the University and its staff more closely to the wider community, especially industry and the public sector at state and federal levels. In seeking to facilitate consultancy activities by University employees, the Policy aims to ensure that appropriate attention is paid to all risk and legal issues, taxation issues insurance coverage, competitive neutrality and protection of the brand and reputation of the University. </p>
                                    <p>Employees can undertake consultancy work: as a University, Negotiated, or Community Service Consultancy as a private individual as a Private Consultancy as permitted by the University.</p>
                                    
