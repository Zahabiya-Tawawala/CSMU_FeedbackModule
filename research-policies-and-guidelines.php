                                        <h4 class="heading-primary bluehead"><strong>IPR Policy</strong></h4>
                                    </div>
                                    <p>Intellectual  property plays an essential role in the teaching and research functions of  universities and public research institutions. Through their research and  development (R&amp;D) activities, universities and public research institutions  produce results in the form of inventions that may be patentable, but are often  no more than a proof of concept of a laboratory-scale prototype, which require  further R&amp;D prior to possible commercialization.</p>
<p>In  addition, the teaching activities of a university or public research institute  generate IP in the form of teaching materials, theses, software and designs.  While the Internet and other modern technologies have fostered greater access  to scholarly materials, these advanced technologies have also given rise to a  growing number of disputes over the ownership and use of these materials.</p>
<p>Chhatrapati  Shivaji Maharaj University frames its own IPR policy to protect and allow the  commercialization of the results of the research produced under its wings.</p>
                                    
