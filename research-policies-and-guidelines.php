                                    <p>Plagiarism is presenting someone  else's work or ideas as your own, with or without their consent, by  incorporating it into your work without full acknowledgement. All published and  unpublished material, whether in manuscript, printed or electronic form, is  covered under this definition. Plagiarism may be intentional or reckless, or  unintentional. Under the regulations for examinations, intentional or reckless  plagiarism is a disciplinary offence.<br />
  The  University's Academic policy Guidelines on Plagiarism is based on the  plagiarism policy circulated by the UGC (<a href="https://www.ugc.ac.in/pdfnews/8864815_UGC-Public-Notice-on-Draft-UGC-Regulations,-2017.pdf" target="_blank">https://www.ugc.ac.in/pdfnews/8864815_UGC-Public-Notice-on-Draft-UGC-Regulations,-2017.pdf</a>) and defines the following as acts of plagiarism:</p>
  <ul>
  <li>Direct copying of paragraphs, sentences, a single sentence or significant parts of a sentence;</li>
  <li>Direct copying of paragraphs, sentences, a single sentence or significant parts of a sentence with an end reference but without quotation marks around the copied text;</li>
  <li>Copying ideas, concepts, research results, computer codes, statistical tables, designs, images, sounds or text or any combination of these;</li>
  <li>Paraphrasing, summarising or simply rearranging another person's words, ideas, etc without changing the basic structure and/or meaning of the text;</li>
  <li>Offering an idea or interpretation that is not one's own without identifying whose       idea or interpretation it is;</li>
  <li>A 'cut and paste' of statements from multiple sources;</li>
  <li>Presenting as independent, work done in collaboration with others;</li>
  <li>Copying or adapting another student's original work into a submitted assessment item.</li>
  <li>Additionally,  by the circular issued by the UGC (<a href="https://www.ugc.ac.in/pdfnews/2284767_self-plagiarism001.pdf" target="_blank">https://www.ugc.ac.in/pdfnews/2284767_self-plagiarism001.pdf</a>) Copying own work without proper citation will be  considered as self- plagiarism.</li>
  </ul>
                                    
                                    
