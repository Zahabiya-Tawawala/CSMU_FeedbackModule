<h4>About CSMUAA</h4>
<p><li>Any university can be shaped and reshaped through the assistance of its alumni. The dynamic nature of technology does not support to structured manpower supply system. Alumni can provide precise insights into the professional requirements of the market, industry, and employers to update or revise the curricula of the university. Therefore, they can help to fill the gap between the university and the potential employment market. Moreover, they can support the university regarding the areas where financial and administrative limitations often obstruct the fulfilment of its vision and mission. A professional body of Chhatrapati Shivaji Maharaj University - trained graduates from various disciplines, termed as CSMU Alumni Association (CSMUAA), has propose to engender and register by faculty members and the graduates of the University from the first batch (2022) itself under the Society Registration Act 21, 1860 and the Charity Commission Act 1950. The CSMU alumni will hold distinguished positions, because the University produce the graduates from multi-discipline faculties and the university collaborates with many national and international organisations including industries, financial institutes and academic institutions. The CSMUAA also propose to organise several activities, including lectures, workshops, and debates for students, encouraging them to connect with eminent personalities who are experienced professionals in various fields.</li>
<p><li><strong>MISSION: </strong> The mission of CSMUAA Office of Alumni Association is to create a lifelong and worldwide community of alumni through increased opportunities for meaningful engagement in order to increase awareness, pride, participation, volunteer involvement, and philanthropic commitment to Chhatrapati Shivaji Maharaj University, Panvel.
</li>
<P><li><strong>VISION: </strong> To promote a goodwill and sense of pride to both alumni and students. To help and support alumnus develop and spread a philanthropic and entrepreneurial mind-set among students. To advice and conduct activities that shall motivate and upgrade student skill sets.
</a>)</li>
<h4>Objectives of CSMUAA</h4>
<p>1. To promote interaction among the CSMUAA members and between the Chhatrapati Shivaji Maharaj University.
<p>2. To encourage, promote and facilitate education, research, training and other academic activities of the disciplines covered in the curricula in general and the University in particular.
<p>3. To disseminate and encourage the study of (i) Engineering; (ii) Science; (iii) Law; (iv) Arts, Humanities and Communication; (v) Pharmacy and (vi) Commerce, Management & Hotel Management in which the university is conducting teaching and research in the above disciplines.
<p>4. To encourage fellowship and greater interaction among members of the CSMUAA in general. 
<p>5. To enable cooperation and render possible assistance to all the professionals engaged in teaching, training, research, governance, administration and other matters related to all the six disciplines listed in the university academic programmes of the university.
<p>6. To bring solidarity and fellowship among professionals engaged in teaching, training, research, governance, administration and other matters involved in all the six disciplines listed in the university academic programmes and other important stakeholders.
<p>7. To liaison, communicate and cooperate with other universities, academic institutions, research organizations, donor agencies, Non-Govt. and Govt. organizations etc. in India and abroad for the purpose of achieving any of the objects for which the CSMUAA is established.
<p>8. To help in establishing educational institutions like departments, schools, colleges, universities, training institutes, etc. to impart knowledge and skills on the given six disciplines and its sub-domains.
<p>9. To organize lectures, seminars, conventions, conferences, workshops, retreats, training programmes and such gatherings for the purpose of achieving any of the objects for which the CSMUAA is established.
<p>10. To prepare, edit, publish, issue, acquire and circulate books, papers, newsletters, pamphlets, posters, scientific journals, e-journals, periodicals, circulars and other literature having a bearing on achieving any of the objects for which the CSMUAA is established.
<p>11. To apply to Government, public bodies like urban, local, municipal, district and other bodies, corporations, associations, companies or other persons/ individuals to obtain and accept grants, money, equipment, land, buildings, donations, gifts, subscriptions and other assistance with a view to promote and fulfil the objects of the CSMUAA.
<p>12. To promote professional development of CSMUAA members and assist to young/fresh members of the Association in career planning and development.

 
