	  <div class="heading heading-border heading-middle-border mt-5" id="course_offered">
        <h4>Programme Educational <strong>Objectives</strong></h4>
      </div>
	  
	  <ul>
	  <li>Recognize the effects of their own personal values, ethics, and beliefs upon their practice.</li>
	  <li>Demonstrate a commitment to the social justice goals of furthering equality in our society.</li>
	  <li>Apply conceptual frameworks to practice with individuals, families, groups, organizations, and communities.</li>
	  <li>Foster change and development at the individual, family, group, organizational, and community levels.</li>
	  <li>Evaluate individual practices.</li>
	  </ul>
	  
	   <div class="heading heading-border heading-middle-border mt-5" id="course_offered">
        <h4>Highlights</h4>
      </div>
	  <p>Teaching and learning methods shall comprise of lectures, demonstration of techniques, group discussions, interactions with domain experts, work with NGOs, studio assignments, presentations, reviews, critiques and feedbacks. It is encouraged that students will come up with their individual ideas to work in society.</p>
	  <p>The evaluation criteria run parallel to the course seeking a continuous comprehensive evaluation of all the competencies required in the area. This incorporates weight age given to theoretical concepts, class participation, projects, internships/industrial visits etc.</p>
	  
	   <div class="heading heading-border heading-middle-border mt-5" id="course_offered">
        <h4>Some of the major areas that will be covered</h4>
      </div>
      <p>Beginning of Social Work, Fundamentals of Social Work, Sociology for Social work, Basic concepts in Psychology, Contemporary Social Concerns, Social Psychology, Working with Individuals, Working with Groups, Social Deviance and Social Problems, NGO Management, Social Welfare & Administration, Social Policy and Social Development, Social Legislation and Human Rights, Social Action & Movements, Community Organization, Communication and Development.</p>
	  
	   <div class="heading heading-border heading-middle-border mt-5" id="course_offered">
        <h4>Career <strong>Avenues</strong></h4>
      </div>
	  <p>Bachelors in Social Work are a step closer to obtaining a Master's degree, which enables one to provide clinical services such as psychotherapies. However, many bachelor's level social workers decide to work in entry level in direct care positions, providing services and assistance to people in need. They can work in different areas ranging from child and family to substance abuse counselors.</p>
	  
