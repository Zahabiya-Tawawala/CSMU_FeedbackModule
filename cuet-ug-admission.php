<h4>Admission Process</h4>
<p>1. Submit Online Application for Admission in the Choice of Programme. You can apply to multiple programmes as per your eligibility 
<p>2. Please select YES in the Application form query whether you have appeared in CUET-UG Examinations, enter your CUET-UG application number.
<p>3. In the Educational Qualification Details page of the online application form, after entering detail of you HS (+2 level) Examination, Select "Yes" on "HAVE YOU APPEARED IN ANY ENTRANCE EXAMINATION?" and fill detail of CUET-UG examination <a href="https://csmu.ac.in/pdf/cuet/cuet-npf.pdf" target="blank"><strong>SEE EXAMPLE</strong></a> 
<p>4. Enter your <strong>Total Normalised Score</strong> in the Score/Percentile field. Put All India Rank "0". <a href="https://csmu.ac.in/pdf/cuet/cuet23mapping.pdf" target="blank"><strong>CLICK HERE</strong></a> to know more detail on calculation of TOTAL NORMALISED SCORE for different programmes.
<p>5. An Admission List will be published in this website and eligible candidates will be contacted via email for counseling and admission.
<p>6. Shortlisted candidates may take admissions by 
<ul> 
  <li>Verification and submission of required documents.</li>
  <li> Payment of at least 50% of the Annual Fees (Please refer the fee structures - <a href="https://csmu.ac.in/programs-offered.php" target="blank">link here</a>) </li>
</ul>
