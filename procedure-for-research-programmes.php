  <li>On/Before the scheduled completion of the coursework Departmental Research  Committee (DRC) shall hold a meeting and allocate supervisor to each student  based on student's research interest, supervisor's specialization and  availability. </li>
  <li>A Professor can take up to 8 PhD&amp; 3 M Phil students, an  Associate Professor can take up to 6 PhD&amp; 2 M Phil and an Assistant  Professor can take up to 4 PhD&amp; 1 M Phil students at a time. </li>
  <li>The Research Scholars are required to meet the Supervisors  allocated to them and take guidance in determining the topic of research and  preparation of a viable Research Plan, which shall be submitted to concern HOD/  Dean within six months after complete of course work. </li>
