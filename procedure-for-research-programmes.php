<label>Course Work </label>
<div class="toggle-content">
                  	<p>The M.Phil./ Ph.D. programmes run under theordinance of the University, which has allowed constant modulation of the programme in response to the changing needs of the students.
Spread over two semesters, the course work of the M. Phil./ Ph. D.  programmes are common and has the following structure:</p>
									<ul>
  <li>The course work for the M. Phil. /  Ph. D. programmes consists of two courses in each of the first two semesters,  totalling to 4 courses and carries 16 credits. Two of these courses are  compulsory, namely Research Methodology and Seminar. The other courses are  subject-specific elective courses (SEC) and should be chosen on the basis of  the research interest of the candidate and the supervisor(s).  </li>
  <li>In the end of the 2nd semester, the  candidate should submit a research proposal containing thorough literature  review and a viable research plan in accordance with their respective programme  (M. Phil. / Ph. D.).</li>
  <li>An M. Phil./Ph. D. scholar has to  obtain a minimum of 55% marks or equivalent grade points  in the course work in order to be eligible to  continue in the programme and submit the dissertation/thesis.</li>
  <li>In the 2nd year (3rd semester),  students work on their research only. The candidates for the M. Phil. programme  should work on their dissertation by writing an individually supervised essay  on the area of their research interest. At the end of the this semester,  interested students may apply for a direct transfer to the Ph. D. programme  without completing their M. Phil. Decisions on such applications take into  account the performance of the student in coursework and the quality and  viability of the research proposal.</li>
  <li>Awarding of grades to research candidates based on their  performance shall be done as per the applicable ordinances and regulations for  undergraduate and Post graduate programs of Engineering under the Faculty of  Technology. Semester Grade Point Average (SGPA) shall be also calculated based  on the formulae stipulated in University Examination Rules and regulations. </li>
  <li>A minimum of 75% attendance of the classes held in each course is  mandatory.  </li>
</ul>  			  			 
