<label>Synopsis and Pre-Submission Seminar (PSS)</label>
<div class="toggle-content">
                	<ol>
  <li>As the Supervisor feels that the thesis can be submitted within  six months he/she shall ask the research scholar to prepare and submit a draft  synopsis containing a summary and outline of chapters. The draft synopsis may  not exceed 5000 words. The scholar shall, on recommendation of the supervisor,  submit 8 copies of the synopsis to him/her for the consideration of the RAC.</li>
  <li>After submission of synopsis scholar shall be required to make an  open presentation of her/his work before the RAC, known as the Pre-Submission  Seminar (PSS) convened by the Supervisor, all the teachers and research  scholars of the Department concerned are to be present in this seminar with a  view to obtain suggestions for possible improvements. </li>
  <li>The Supervisor shall forward the copies of the draft synopsis to  the members of the RAC at least two weeks before the Pre-submission Seminar  (PSS).</li>
  <li>The written report of RAC on PSS containing suggestions and  observations that shall be communicated to the research scholar through the  Supervisor within one week from the date of the PSS to be incorporated in the  synopsis and the thesis. </li>
  <li>A Ph.D. scholar is required  to submit the thesis within six months (but not earlier than one month) from  the date of his/her pre-submission seminar, failing which he/she shall be  required to deliver a fresh pre-submission seminar. </li>
</ol>
