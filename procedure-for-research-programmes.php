<section class="toggle">
<label>Research Advisory Committee (RAC)</label>
<div class="toggle-content">
                  	<ol>
  <li>The Research Scholar shall be required to defend his/her Research  Plan in a presentation before the concerned Research advisory Committee (RAC)  provided he/she has completed the course work.</li>
  <li>By the advice of the supervisor, the scholar shall fill the  application form for the RAC and submit to the supervisor, along with suggested  number of copies of research plan, no objection certificate (if working professional, i.e. part  time scholars), migration certificate (if not submitted earlier), enrolment  form and fees receipt to date. </li>
  <li>Upon satisfying the RAC with the corrections suggested if any, the  Research Scholar shall submit four copies of finalised Research Plan to the  Board of Research and Development (BRD) through the Supervisor..</li>
  <li>If the RAC is not satisfied with the Research Plan and other  documents submitted by the Research Scholar, it shall advise the candidate,  through the Supervisor, along with necessary suggestions to submit a revised  Research Plan for reconsideration within 60 days from the date of presentation.</li>
  <li>Based on the recommendation of the RAC, The registration in the M.  Phil./ Ph.D. programmes shall be confirmed by the Board of Research &amp; Development  (BRD) subject to clearance of dues towards the University.</li>
  <li>The research scholar shall appear before the RAC once in six  months to make a presentation on the progress on his/her work for evaluation  and further guidance. The six monthly progress reports with the comments of the  RAC shall be submitted to the BRD.</li>
  <li>If there are two consecutive &lsquo;'Unsatisfactory' (US)&rsquo;  reports, the RAC may recommend to the BRD to terminate the registration of the  scholar. </li>
  <li>M.Phil. scholars shall present at least  one research paper in a conference/seminar and Ph.D. scholars shall publish at  least two researchpapersin peer-reviewed journals (Only SCI/ SCOPUS/ WEB OF  SCIENCE/ UGC care listed) and make two paper presentations in conferences/seminars  before the submission of the dissertation/thesis for evaluation, and produce  evidence for the same in the form of presentation certificates and/or reprints/  acceptance letter from the publisher/ editor. This evidence has to be certified  by the Supervisor and the Head of concerned Department. Guidelines for research  publications issued by the University shall be displayed on the official  website of the University and shall be updated, from time to time. </li>
  <li>Credits may be awarded for high-quality  publications as per the guidelines issued by the UGC or other equivalent  regulatory body from time to time. </li>
  <li>Submission of progress report and semester seminars  should continue till submission of thesis. </li>
</ol>
