<div class="toggle-content">
	<div class="heading mt-3 mb-3">
                                        <h4 class="heading-primary bluehead">1. Call for Applications</h4>
                                    </div>    
                                    <p>Admission to the M. Phil./ Ph. D. programme shall be made bi-annually as per the admission policy approved by the Academic Council of the University from time to time. University will issue a notice and call for applications for admissions in M. Phil. and Ph. D. programmes. The names of the available disciplines and prominent interdisciplinary topics will be published on the university website from time to time.</p>

                                    <div class="heading mt-3 mb-3" >
                                        <h4 class="heading-primary bluehead">2. Entrance Test</h4>
                                    </div>
<p>The applicants for both the M.Phil. and Ph. D. programmes who fulfill the eligibility criteria must be put through thePhD Entrance Test (PET) - a written test conducted by the University followed by a process of interview.</p>
                                                        <ul>
                                                        	<li>Candidates qualified in UGC-NET (Including JRF)/UGC-CSIR NET (Including JRF/SLET/GATE (valid score) or teacher fellowship holders will be exempted from the Entrance Test for M.Phil./ Ph.D. programmes. Candidates who have passed M. Phil. Programme of the University will be exempted from the PET for Ph.D. programme.</li>
                                                            <li>PET shall be qualifying with qualifying marks as 50%. The syllabus of the PET shall consist of 50% of research methodology and 50% shall be subject specific. </li>
                                                            <li>PET will consist ofa 100 marks- multiple-choice-type question paper, out of which, 50 questions shall contain general knowledge, research methodology and aptitude related questions (Section-A) whereas50 questions shall be shall be subject specific (Section-B).</li>
                                                        </ul>
<div class="heading mt-3 mb-3" >
    <h4 class="heading-primary bluehead">3. Interview</h4>
</div>
<ul>
    <li>An interview shall be organizedfor the candidatesqualified in PET and the ones exempt from it where the candidates are required to discuss their research interest/area through a presentation before a duly constituted Interview Committee for this purpose. </li>
    <li>The interview shall also consider the following aspects, viz. whether 
    <ul>
        <li>The candidates possess the competence for the proposed research. </li>
        <li>The research work can be suitably undertaken at the Department/Institutions. </li>
        <li>The proposed area of research can contribute to new/additional knowledge. </li>
    </ul>
    </li>
    <li>During the Interview, the applicant will be assessed on his interest and motivation in doing research, conceptual clarity on chosen area of research and his past academic and research performances.</li>
</ul>
<div class="heading mt-3 mb-3" >
    <h4 class="heading-primary bluehead">4. Publication of Merit List</h4>
</div>
<ul>
<li>Applicants shall be short listed on the basis of merit for the predetermined seats available in each Department/Faculty for M. Phil./ Ph.D. programmes of the University in an academic session.</li>
<li>Selected Candidates shall be asked to join within a period of one month from the date of the offer made to them. Under exceptional circumstances, an additional one month period may be given to the selected candidates to join the program.
<li>Vacancies, arising out of any candidate's failure to join within the stipulated time, shall be filled from amongst the candidates in the merit list.</li>
</ul>
<div class="heading mt-3 mb-3" >
    <h4 class="heading-primary bluehead">5. Admission</h4>
</div>
<p>Final shortlisted candidates will take admission in the University by filling up the admission form, have their original document verified and by paying the stipulated fee.</p>  
