<section class="toggle">
<label>Thesis/ Dissertation Submission </label>
<div class="toggle-content">
                  <p>A thesis should be supplemented by published work, in refereed journals and in notable conferences. A scholar may submit his thesis within the time period as stipulated in rules provided that:</p>
<ol>
  <li>The scholar shall initially submit three copies of the thesis/  dissertation with a soft cover. In case of co-supervision four hardcopies and  one electronic copy in CD/USB stick of thesis/ dissertation shall be submitted  by the scholar.</li>
  <li>The thesis/ dissertation shall be put through anti plagiarism  software to detect any kind of Academic dishonesty. </li>
  <li>For this purpose, the University shall evolve a mechanism using  well-developed software and gadgets prescribed by the UGC to detect plagiarism  and other forms of academic unethical practices. </li>
  <li>Each thesis/ dissertation will go through a Plagiarism Check that  will be verified by the University Library before submission. The certificate  of verification given by the library has to be submitted along with the thesis/  dissertation at the time of thesis submission. </li>
  <li>The thesis/ dissertation has to be submitted within 30 days from  the date of plagiarism check.</li>
  <li>While submitting for evaluation, the thesis/ dissertation shall  have an undertaking from the research scholar and a certificate from the  Research Supervisor attesting to the originality of the work, vouching that  there is no plagiarism and that the work has not been submitted for the award  of any other degree of the same Institution where the work was carried out, or  to any other Institution. </li>
  <li>The Supervisor and Head/ Dean of concerned department shall  countersign these certificates for submission of the thesis/ dissertation. In  case of resubmission the same procedure may be followed. </li>
  <li>The research scholar shall also attach a Student Approval Form in  the format provided by the UGC for hosting and distributing their thesis/  dissertation in digital format in ·shodhganga', or any other server designated  for this purpose by UGC.</li>
  <li>The thesis/dissertation shall be in the English Language, except  for those submitted in the Departments of Hindi, Marathi, Modern Indian  Languages, Persian, Sanskrit and Urdu etc, in which the thesis/dissertation  shall be written in respective language or language approved by the BRD on the  recommendation of RAC.</li>
  <li>The research scholar shall submit the thesis/dissertation, along  with the prescribed proforma, to the Dean, R&amp;D/ Controller of Examinations,  duly forwarded by Supervisor(s), the Head of the Department concerned and Dean  of the Faculty.</li>
  <li>A Ph.D. student shall, along with his/her thesis, also submit four  hard copies and one soft copy in PDF format (in a CD) of the abstract of the  thesis written in about 600 words describing the salient features of his/her  investigation.</li>
</ol>                    
