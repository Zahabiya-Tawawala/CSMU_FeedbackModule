        <h4>
          <strong>Training & Placement Process</strong>
        </h4>
      </div>
      <p>
        <strong>Vision:</strong> To flourish the professional progress of students by integrating the best employability skills within an academic environment and leading them towards the bright career.
      </p>
      <p>
        <strong>Mission:</strong> To raise the knowledge and skills of the students, and to partner with the renowned organization in the industry to provide best career opportunities for CSMU students in India and abroad.
      </p>
      <p>
        <strong>Purpose:</strong> The purpose of the Training & Placement Cell is to enhance the employability of the students and to match the corporate expectations by providing them career guidance and training program, enable to gain students competitive edge in the recruitment cycle, groom their confidence and developing their personalities via Soft Skills, Aptitude and Technical Training.
      </p>
      <div class="heading heading-border heading-middle-border mt-5 mb-2" id="">
        <h4>
          <strong>Training</strong>
        </h4>
      </div>
      <p>The students are trained to attain placement through a variety of programmes run by the Placement & Training Cell of CSMU such as:</p>
      <ul>
        <li>The Training programs have been formulated after a thorough analysis of unique requirement of various companies as the required skills will vary according to the company's requirement, market demands and state of the art tools and soft skills</li>
        <li>Guest lecturer sessions are organized with the help of industry experts to bridge the gap between industry &amp; academia.</li>
        <li>Various Training Programmes are organized to train the students in the areas of Aptitude, Quantitative Reasoning, Logical Reasoning and Verbal through the Reputed External Training centers. Imparting training program with the help of reputed training Institutes, enable the students to gain a competitive edge in the recruitment process, groom their confidence and developing their personality.</li>
        <li>Technical training programmes areÂ  conducted by Technical experts.</li>
        <li>Language learning programmes to train students in developing their communication skills and foreign language skills.</li>
        <li>Training through, Group Discussions and Mock Interviews for students to perform well in the professional interviews as per the expectations of the corporate world.</li>
      </ul>
      <div class="heading heading-border heading-middle-border mt-5 mb-2" id="">
        <h4>
          <strong>Shortlisting</strong>
        </h4>
      </div>
      <ul>
        <li>Companies may do shortlisting of students themselves on the basis of information supplied or the criteria set for the students to apply for respective company recruitment drive, information provided by the students in their registration profile or CVs/ Resumes.</li>
        <li>In case a company insists on short listing to be done by the Institute without explicitly citing any criteria, it will be done on the basis of Criteria determined in consultation with the HOD of respective department and Training and Placement Officer.</li>
      </ul>
      <p>
        <strong>Students Shall</strong>
      </p>
      <ul>
        <li>Keep record of organizations and positions for which they apply.</li>
        <li>Keep notes on the job details announced. These are useful at the time of interview.</li>
        <li>Prepare completely for attending the various Written Tests, Group Discussions and Interviews, particularly in respect of the specific company for which they are appearing. Study the website of the company &amp; their competitors.</li>
        <li>Students shall not, at the time of interview, negotiate with the employer about salary and terms different from what is announced earlier, unless the announcement specifies that the salary is negotiable.</li>
        <li>While attending interviews, students must be punctual and come in proper uniform/ prescribed dress code only.</li>
        <li>It is compulsory for every student who has applied for a particular company, to attend the Pre Placement Talk (PPT) of that company.</li>
      </ul>
      <p>
        <strong>Withdrawal Procedure</strong>
      </p>
      <p>Student who has applied for a particular company can withdraw from the company after the Pre Placement Talk. Once the selection procedure has started, students cannot withdraw at any stage, unless they have a final offer from another organization during the period of registration and interview process. It is presumed that students would apply for a position after careful consideration of all the relevant aspects.</p>
      <p>If a student does not appear for interview after giving the nomination and confirmation after PPT, he/she is deemed to have withdrawn.</p>
      <div class="heading heading-border heading-middle-border mt-5 mb-2" id="">
        <h4>
          <strong>Conclusion</strong>
        </h4>
      </div>
      <ul>
        <li>CRC shall aim to provide placement assistance for all graduating students. Placement is a privilege extended to the students not a right.</li>
        <li>Any breach of rules specified above by any student, shall be taken up seriously by the CRC who in turn will view the matter and take action against the student, as it may deem fit.</li>
        <li>The CRCreserves the right of modifying any or all of the above norms and/or stipulating additional norms for placement which, in its judgment and discretion, are likely to benefit the students, immediately or in the future.</li>
      </ul>
      <p>Final authority in case of any dispute would be the Vice-Chancellor/ Registrar / CSMUTPO and his/her decision will be final.</p>
