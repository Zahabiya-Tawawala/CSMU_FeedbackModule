	<h4><strong>Pharmaceutical Analysis Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of the course student shall be able to </p>
    <ul>
  <li>Understand  the principles of volumetric and electro chemical analysis </li>
  <li>Carryout  various volumetric and electrochemical titrations </li>
  <li>Develop  analytical skills </li>
</ul>
	<h4><strong>Pharmaceutics Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of this course the student should be able to</p>
    <ul>
  <li>Know the  history of profession of pharmacy </li>
  <li>Understand  the basics of different dosage forms, pharmaceutical incompatibilities and  pharmaceutical calculations. </li>
  <li>Understand  the professional way of handling the prescription </li>
  <li>Preparation  of various conventional dosage forms </li>
</ul>
	<h4><strong>Pharmaceutical Inorganic Chemistry Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of the course student shall be able to</p>
    <ul>
    	<li>Know the sources of impurities and methods to determine the impurities in inorganic drugs and pharmaceuticals.</li>
		<li>Understand the medicinal and pharmaceutical importance of inorganic compounds</li>
    </ul>
    <h4><strong>Pharmaceutical Organic Chemistry Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of the course the student shall be able to</p>
    <ul>
  <li>Write  the structure, name and the type of isomerism of the organic compound </li>
  <li>Write  the reaction, name the reaction and orientation of reactions </li>
  <li>Account  for reactivity/stability of compounds, identify/confirm the identification of  organic compound </li>
  <li>Synthesis  based on various name reactions. </li>
</ul>
    <h4><strong>Biochemistry Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of course student shell able to</p>
    <ul>
  <li>Understand  the catalytic role of enzymes, importance of enzyme inhibitors in design of new  drugs, therapeutic and diagnostic applications of enzymes. </li>
  <li>Understand  the metabolism of nutrient molecules in physiological and pathological conditions.</li>
  <li>Understand  the genetic organization of mammalian genome and functions of DNA in the  synthesis of RNAs and proteins. </li>
  <li>Stability  studies of Biological products. </li>
</ul>
	<h4><strong>Pharmaceutical Microbiology Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of the subject student shall be able to</p>
    <ul>
  <li>Understand  methods of identification, cultivation and preservation of various  microorganisms. </li>
  <li>To  understand the importance and implementation of sterlization in pharmaceutical  processing and industry. </li>
  <li>Learn  sterility testing of pharmaceutical products. </li>
  <li>Carry  out microbiological standardization of Pharmaceuticals. </li>
  <li>Understand the cell culture  technology and its applications in pharmaceutical industries.</li>
</ul>
    <h4><strong>Pharmaceutical Engineering Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of the course student shall be able</p>
    <ul>
  <li>To know  various unit operations used in Pharmaceutical industries. </li>
  <li>To  understand the material handling techniques. </li>
  <li>To  perform various processes involved in pharmaceutical manufacturing process. </li>
  <li>To carry  out various test to prevent environmental pollution. </li>
  <li>To  appreciate and comprehend significance of plant lay out design for optimum use  of resources. </li>
  <li>To appreciate the various  preventive methods used for corrosion control in Pharmaceutical industries.</li>
</ul>
    <h4><strong>Medicinal Chemistry Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of the course the student shall be able to</p>
    <ul>
  <li>Understand  the chemistry of drugs with respect to their pharmacological activity </li>
  <li>Understand  the drug metabolic pathways, adverse effect and therapeutic value of drugs </li>
  <li>Know the  Structural Activity Relationship (SAR) of different class of drugs </li>
  <li>Write  the chemical synthesis of somedrugs </li>
</ul>
    <h4><strong>Physical Pharmaceutics Lab</strong></h4>
    <p><strong>Objective:</strong> Upon the completion of the course student shall be able to</p>
    <ul>
    	<li>Understand various physicochemical properties of drug molecules in the designing the 
dosage forms</li>
        <li>Know the principles of chemical kinetics & to use them for stability testing and determination of expiry date of formulations</li>
        <li>Demonstrate use of physicochemical properties in the formulation development and 
evaluation of dosage forms </li>
    </ul>
    <h4><strong>Pharmacology Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of this course the student should be able to</p>
    <ul>
  <li>Understand the pharmacological actions of different categories of drugs </li>
  <li>Explain the mechanism of drug action at organ system/sub cellular/ macromolecular levels.</li>
  <li>Apply the basic pharmacological knowledge in the prevention and treatment of various diseases.</li>
  <li>Observe the effect of drugs on animals by simulated experiments </li>
  <li>Appreciate correlation of pharmacology with other bio medical sciences </li>
</ul>
    <h4><strong>Pharmacognosy and Phytochemistry Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of the course, the student shall be able</p>
    <ul>
  <li>To know  the techniques in the cultivation and production of crude drugs </li>
  <li>To know  the crude drugs, their uses and chemical nature </li>
  <li>Know the  evaluation techniques for the herbal drugs </li>
  <li>To carry  out the microscopic and morphological evaluation of crude drugs </li>
</ul>
    <h4><strong>Biopharmaceutics and Pharmacokinetics Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of the course student shall be able to</p>
    <ul>
  <li>Understand the basic concepts in biopharmaceutics and pharmacokinetics and their 
significance. </li>
  <li>Use of plasma drug concentration-time data to calculate the pharmacokinetic parameters 
to describe the kinetics of drug absorption, distribution, metabolism, excretion, 
elimination.</li>
  <li>To understand the concepts of bioavailability and bioequivalence of drug products and 
their significance.</li>
  <li>Understand various pharmacokinetic parameters, their significance & applications</li>
</ul>
    <h4><strong>Pharmaceutical Biotechnology Lab</strong></h4>
    <p><strong>Objective:</strong> Upon completion of the subject student shall be able to</p>
    <ul>
  <li>Understanding  the importance of Immobilized enzymes in Pharmaceutical Industries </li>
  <li>Genetic  engineering applications in relation to production of pharmaceuticals </li>
  <li>Importance  of Monoclonal antibodies in Industries. </li>
  <li>Appreciate  the use of microorganisms in fermentation technology </li>
</ul>
    <h4><strong>Dietary Supplements And Nutraceuticals Lab</strong></h4>
    <p><strong>Objective:</strong> This module aims to provide an understanding of the concepts behind the 
theoretical applications of dietary supplements. By the end of the course, students should 
be able to</p>
    <ul>
  <li>Understand  the need of supplements by the different group of people to maintain healthy  life. </li>
  <li>Understand  the outcome of deficiencies in dietary supplements.. </li>
  <li>Appreciate  the components in dietary supplements and the application. </li>
  <li>Appreciate the regulatory and  commercial aspects of dietary supplements including health claims.</li>
</ul>
    
    
