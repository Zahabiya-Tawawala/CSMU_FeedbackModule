										<ul>
										<li>All hard furnishings such as Fans, Bulbs, Tubes, Tables, Chairs, Cots and Almiras will be issued by the University to the students at the time of occupying the room. The students have to return all these in good condition at the time of leaving the room after the end of Academic Session.</li>
										<li>No student is allowed to stay in the hostel during working hours.</li>
										<li>Any hosteller, if found staying in the hostel during his/her class hours will be fined on the first offence and thereafter strict action will be taken against him/her.</li>
										<li>Hostellers are not allowed to remain outside the hostel after 10.00 P.M. (For Girls not beyond 07.00 P.M.)</li>
										<li>Possession, distribution and consumption of alcoholic beverages, prohibited drugs, chewable tobacco and smoking in the hostel is strictly prohibited and Students found involved in these activities will be fined and may be expelled from the hostel.</li>
										<li>Day Scholars or non-hostellers are not allowed in hostels.</li>
										<li>A hostel campus should be a place where students can have the best possible conditions for studies and adequate rest. As such, due consideration must be accorded to other hostelites at all times. Noise level must be kept low to allow others the opportunity to study or sleep in comfort. Television, Radio etc provided in the common room must be switched off or volume toned down after 10:00 pm. These rules are intended to ensure a tranquil environment for all hostelites.</li>
										<li>Foreign / NRI students may reserve their rooms while submitting their application forms. Reservation will be based on their registration for the course with the admissions office. Foreign/NRI students will be required to submit self-attested photocopy of their passport along with their visa and relevant documents (e.g. Form C).</li>
										<li>Allotment made to a student is subject to cancellation if he/she fails to occupy the room in the prescribed time. Students will also forfeit their rooms if they fail to clear all their hostel dues before the due date. In such cases, they will be asked to vacate the hostel.</li>
