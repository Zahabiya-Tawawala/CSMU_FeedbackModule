<h3><strong>3. Eligibility criteria for admission to Ph.D. programme:</strong></h3>

<p>Candidates with a Master's degree or equivalent are eligible to apply for the Ph.D. programme with requisite marks / grade as specified by UGC (minimum standards and procedures for award of M.Phil. and Ph.D.) Regulations 2022 and its subsequent amendments.</p>
<h3><strong>4. PhD Entrance Test (PET)</strong></h3>
<p>The applicants Ph.D. programmes who fulfill the eligibility criteria must be put through the Ph.D. Entrance Test (PET) - an offline written test conducted by the University followed by a process of interview by a Panel constituted for this purpose. An appearing list will be published in the University notice board and website for the PET.</p>
<p>Candidates qualified in UGC-NET (Including JRF)/UGC-CSIR NET (Including JRF/SLET/GATE valid score) or LS fellowship holders and candidates who have M. Phil. degree will be exempted from PET. The Syllabus of PET is the same with the syllabus of the latest NET examination in the concerned subject.</p>

<h3><strong>5. Interview</strong></h3>
<p>An interview shall be organized for the candidates qualified in PET and the ones exempt from it where the candidates are required to discuss their research interest/area through an online presentation. An appearing list for the Interview shall be published in the University notice board and website.&nbsp;</p>

<h3><strong>6. Offer of Admission</strong></h3>
<ol>
<li>A merit list for admission shall be published based on the overall results of the PET and the interview. Applicants shall be short listed on the basis of merit only and depending on the predetermined seats available in each discipline</li>
<li>Selected Candidates shall be asked to join within a scheduled last date.&nbsp;</li>
<li>Vacancies, arising out of any candidate&rsquo;s failure to join within the stipulated time, shall be filled from amongst the candidates in the merit list.</li>
</ol>
